import UIKit

extension UIColor {
    
    static func pinkColor() -> UIColor {
        return UIColor(red: 1, green: 0, blue: 1, alpha: 1)
    }
    
    static func tealColor() -> UIColor {
        return UIColor(red: 0, green: 1, blue: 1, alpha: 1)
    }
    
    static func kazanColor() -> UIColor {
        return UIColor(red: 0.0767, green: 0.4052, blue: 0.4544, alpha: 1)
    }
    
    static func babuColor() -> UIColor {
        return UIColor(red: 0.1511, green: 0.8081, blue: 0.7320, alpha: 1)
    }
    
}